#!/usr/bin/env bash

# Run a small LP dry-run overhead matrix.
#
# This is intentionally small and sequential. It does not broaden the benchmark
# campaign beyond the schedulers and seeds declared in the config file.
# It intentionally does not use 'set -euo pipefail'.

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
EXPERIMENT_DIR="$(cd "${SCRIPT_DIR}/.." && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../../.." && pwd)"

source "${REPO_ROOT}/experiments/common/env.sh"
source "${REPO_ROOT}/experiments/common/run_lib.sh"

CONFIG="${EXPERIMENT_DIR}/configs/qwen3_0_6b_small_matrix.env"
DRY_RUN="0"
READINESS_TIMEOUT_SECONDS="240"

usage() {
  cat <<'EOF'
Usage:
  run_matrix.sh [options]

Options:
  --config PATH                 matrix config file; default: configs/qwen3_0_6b_small_matrix.env
  --readiness-timeout-seconds N server readiness timeout; default: 240
  --dry-run                     print planned runs, but do not start servers or benchmarks
  -h, --help                    show this help
EOF
}

while [ "$#" -gt 0 ]; do
  case "$1" in
    --config)
      CONFIG="$2"
      shift 2
      ;;
    --readiness-timeout-seconds)
      READINESS_TIMEOUT_SECONDS="$2"
      shift 2
      ;;
    --dry-run)
      DRY_RUN="1"
      shift
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      echo "ERROR: unknown argument for run_matrix.sh: $1" >&2
      usage >&2
      exit 2
      ;;
  esac
done

if [ ! -f "${CONFIG}" ]; then
  echo "ERROR: config file not found: ${CONFIG}" >&2
  exit 1
fi

# shellcheck disable=SC1090
source "${CONFIG}"

if [ -z "${MATRIX_SCHEDULERS:-}" ]; then
  echo "ERROR: MATRIX_SCHEDULERS is empty or unset in ${CONFIG}" >&2
  exit 1
fi

if [ -z "${MATRIX_SEEDS:-}" ]; then
  echo "ERROR: MATRIX_SEEDS is empty or unset in ${CONFIG}" >&2
  exit 1
fi

MATRIX_ROOT="${EXPERIMENT_DIR}/runs/$(experiment_make_run_id)_matrix"
mkdir -p "${MATRIX_ROOT}"

wait_for_server_ready() {
  local host="$1"
  local port="$2"
  local timeout="$3"
  local elapsed="0"

  while [ "${elapsed}" -lt "${timeout}" ]; do
    if curl -sS --max-time 5 "http://${host}:${port}/v1/models" >/dev/null 2>&1; then
      return 0
    fi

    sleep 2
    elapsed="$((elapsed + 2))"
  done

  return 1
}

make_seed_config() {
  local out_config="$1"
  local seed_value="$2"

  {
    echo "# Derived per-run config generated by run_matrix.sh"
    echo "# Source config: ${CONFIG}"
    echo
    echo "MODEL=${MODEL@Q}"
    echo "SERVED_MODEL_NAME=${SERVED_MODEL_NAME@Q}"
    echo "TOKENIZER=${TOKENIZER@Q}"
    echo
    echo "HOST=${HOST@Q}"
    echo "PORT=${PORT@Q}"
    echo
    echo "MAX_MODEL_LEN=${MAX_MODEL_LEN@Q}"
    echo "MAX_NUM_BATCHED_TOKENS=${MAX_NUM_BATCHED_TOKENS@Q}"
    echo "MAX_NUM_SEQS=${MAX_NUM_SEQS@Q}"
    echo "GPU_MEMORY_UTILIZATION=${GPU_MEMORY_UTILIZATION@Q}"
    echo
    echo "NUM_PROMPTS=${NUM_PROMPTS@Q}"
    echo "RANDOM_INPUT_LEN=${RANDOM_INPUT_LEN@Q}"
    echo "RANDOM_OUTPUT_LEN=${RANDOM_OUTPUT_LEN@Q}"
    echo "MAX_CONCURRENCY=${MAX_CONCURRENCY@Q}"
    echo "REQUEST_RATE=${REQUEST_RATE@Q}"
    echo "SEED=${seed_value@Q}"
  } > "${out_config}"
}

echo "=== run_matrix plan ==="
echo "config=${CONFIG}"
echo "matrix_root=${MATRIX_ROOT}"
echo "schedulers=${MATRIX_SCHEDULERS}"
echo "seeds=${MATRIX_SEEDS}"
echo "dry_run=${DRY_RUN}"

for scheduler in ${MATRIX_SCHEDULERS}; do
  for seed in ${MATRIX_SEEDS}; do
    workload_label="${MATRIX_WORKLOAD_LABEL_PREFIX:-tiny}_seed${seed}"
    run_dir="${MATRIX_ROOT}/${scheduler}_seed${seed}"
    derived_config="${run_dir}/config.env"

    echo
    echo "=== matrix cell ==="
    echo "scheduler=${scheduler}"
    echo "seed=${seed}"
    echo "workload_label=${workload_label}"
    echo "run_dir=${run_dir}"

    if [ "${DRY_RUN}" = "1" ]; then
      echo "dry_run=1"
      continue
    fi

    mkdir -p "${run_dir}"
    make_seed_config "${derived_config}" "${seed}"

    "${SCRIPT_DIR}/start_server.sh" \
      --config "${derived_config}" \
      --scheduler "${scheduler}" \
      --run-dir "${run_dir}"

    start_rc="$?"
    if [ "${start_rc}" != "0" ]; then
      echo "ERROR: start_server failed for scheduler=${scheduler} seed=${seed}" >&2
      exit "${start_rc}"
    fi

    if ! wait_for_server_ready "${HOST}" "${PORT}" "${READINESS_TIMEOUT_SECONDS}"; then
      echo "ERROR: server did not become ready for scheduler=${scheduler} seed=${seed}" >&2
      "${SCRIPT_DIR}/stop_server.sh" --run-dir "${run_dir}" || true
      exit 1
    fi

    "${SCRIPT_DIR}/run_one_workload.sh" \
      --config "${derived_config}" \
      --run-dir "${run_dir}" \
      --scheduler "${scheduler}" \
      --workload-label "${workload_label}"

    bench_rc="$?"

    "${SCRIPT_DIR}/analyze_run.sh" \
      --run-dir "${run_dir}"

    analyze_rc="$?"

    "${SCRIPT_DIR}/make_record.sh" \
      --run-dir "${run_dir}" \
      --title "LP Dry-Run Overhead Matrix Run: ${scheduler} seed ${seed}"

    record_rc="$?"

    "${SCRIPT_DIR}/stop_server.sh" \
      --run-dir "${run_dir}"

    stop_rc="$?"

    if [ "${bench_rc}" != "0" ]; then
      echo "ERROR: benchmark failed for scheduler=${scheduler} seed=${seed}" >&2
      exit "${bench_rc}"
    fi

    if [ "${analyze_rc}" != "0" ]; then
      echo "ERROR: analysis failed for scheduler=${scheduler} seed=${seed}" >&2
      exit "${analyze_rc}"
    fi

    if [ "${record_rc}" != "0" ]; then
      echo "ERROR: record generation failed for scheduler=${scheduler} seed=${seed}" >&2
      exit "${record_rc}"
    fi

    if [ "${stop_rc}" != "0" ]; then
      echo "ERROR: stop_server failed for scheduler=${scheduler} seed=${seed}" >&2
      exit "${stop_rc}"
    fi

    echo "matrix_cell_completed scheduler=${scheduler} seed=${seed} run_dir=${run_dir}"
  done
done

echo
echo "matrix_root=${MATRIX_ROOT}"
echo "run_matrix_completed"